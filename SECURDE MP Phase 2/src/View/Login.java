    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String pass = new String(jPasswordField1.getPassword());
        
        //if login credentials are valid
        if(frame.checkLoginCredentials(jTextField1.getText(), pass) && frame.isLockedOut(jTextField1.getText()) == false){
            //depending on role, show the corresponding home
            switch(frame.getRole(jTextField1.getText())){
                case 0: break;
                case 1: break;
                case 2: frame.clientHome();
                        break;
                case 3: frame.staffHome();
                        break;
                case 4: frame.managerHome();
                        break;
                case 5: frame.adminHome();
                        break;
            }
            
            String date = new Timestamp(new Date().getTime()).toString();
            
            if(sqlite.DEBUG_MODE == 1){
                sqlite.addLogs("NOTICE", jTextField1.getText(), "Successful Login", date);
            }
            
            for(User user : sqlite.getUsers()){
                if(user.getUsername().equals(jTextField1.getText())){
                    frame.setCurrentUser(user);
                }
            }
        }
        else{
            //Invalid login credentials
           if(frame.isLockedOut(jTextField1.getText()) == true){ //if user exceeded max attempts
                System.out.println("MAX ATTEMPTS");
                jLabel2.setText("<html>Account has been locked due to exceeding maximum<br/>number of login attempts.</html>" );
           }
           else{
               System.out.println("Regular error message");
               jLabel2.setText("Invalid credentials entered.");
           }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

